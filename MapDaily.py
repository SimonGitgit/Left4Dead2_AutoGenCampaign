def Get_Map_V2():
    import requests
    from bs4 import BeautifulSoup

    # Make a GET request to the Steam Community page
    url = 'https://steamcommunity.com/sharedfiles/filedetails/?id=1951255730'
    response = requests.get(url)

    # Parse the HTML content using BeautifulSoup
    soup = BeautifulSoup(response.content, 'html.parser')

    # Find all the `a` elements with the `href` attribute that includes "https://steamcommunity.com/sharedfiles/filedetails/?id="
    elements = soup.find_all('a', href=lambda href: href and 'https://steamcommunity.com/sharedfiles/filedetails/?id=' in href)

    # Print the href attribute of each element
    for element in elements:
        print(element['href'])
        with open("filename.txt","a") as f:
            f.write(element['href']+"\n")
            f.close()

